  def glyph(cp), do: Map.get(@f, cp, Map.fetch!(@f, ?\s))

  def text_w(str), do: String.length(str) * 6 - 1
  def text_2x_w(str), do: String.length(str) * 12 - 2

  def text(fb, x, y, str, color, a \\ 1.0) do
    str
    |> String.upcase()
    |> String.to_charlist()
    |> Enum.reduce({fb, x}, fn cp, {acc, cx} -> {char(acc, cx, y, cp, color, a), cx + 6} end)
    |> elem(0)
  end

  def text_center(fb, cx, y, str, color, a \\ 1.0),
    do: text(fb, round(cx - text_w(str) / 2), y, str, color, a)

  def text_right(fb, x_right, y, str, color, a \\ 1.0),
    do: text(fb, x_right - text_w(str), y, str, color, a)

  defp char(fb, x, y, cp, color, a) do
    for {bits, row} <- Enum.with_index(glyph(cp)),
        col <- 0..4//1,
        (bits &&& bsl(1, 4 - col)) != 0,
        reduce: fb do
      acc -> FB.add(acc, x + col, y + row, color, a)
    end
  end

  def text_2x(fb, x, y, str, color, a \\ 1.0) do
    str
    |> String.upcase()
    |> String.to_charlist()
    |> Enum.reduce({fb, x}, fn cp, {acc, cx} -> {char_2x(acc, cx, y, cp, color, a), cx + 12} end)
    |> elem(0)
  end

  defp char_2x(fb, x, y, cp, color, a) do
    for {bits, row} <- Enum.with_index(glyph(cp)),
        col <- 0..4//1,
        (bits &&& bsl(1, 4 - col)) != 0,
        reduce: fb do
      acc -> FB.rect(acc, x + col * 2, y + row * 2, 2, 2, color, a)
    end
  end

  @doc "Knockout text — hard `set` so letters punch through a filled bar."
  def knock(fb, x, y, str, color) do
    str
    |> String.upcase()
    |> String.to_charlist()
    |> Enum.reduce({fb, x}, fn cp, {acc, cx} ->
      acc =
        for {bits, row} <- Enum.with_index(glyph(cp)),
            col <- 0..4//1,
            (bits &&& bsl(1, 4 - col)) != 0,
            reduce: acc do
          a2 -> FB.set(a2, cx + col, y + row, color)
        end

      {acc, cx + 6}
    end)
    |> elem(0)
  end

  def knock_w(str), do: String.length(str) * 6 - 1
end
