  @moduledoc """
  5×7 uppercase bitmap font + text helpers, ported verbatim from `F` and the
  draw helpers in `priv/static/matrix/fonts.js`. Bit 4 (0x10) is the leftmost
  column; 7 rows; glyph advance is 6 px (5 wide + 1 gap).
  """
  import Bitwise
  alias WorldCupTracker.Matrix.Framebuffer, as: FB

  @f %{
