const int FONT_COUNT = sizeof(FONT) / sizeof(FONT[0]);

inline char upper(char c) { return (c >= 'a' && c <= 'z') ? (char)(c - 32) : c; }

const uint8_t* glyph(char c) {
  c = upper(c);
  for (int i = 0; i < FONT_COUNT; i++)
    if (FONT[i].c == c) return FONT[i].rows;
  return FONT[0].rows;  // space
}

void drawChar(Fb& fb, int x, int y, char c, Rgb color, float a) {
  const uint8_t* g = glyph(c);
  for (int row = 0; row < 7; row++) {
    uint8_t bits = g[row];
    for (int col = 0; col < 5; col++)
      if (bits & (1 << (4 - col))) fb.add(x + col, y + row, color, a);
  }
}

void drawChar2x(Fb& fb, int x, int y, char c, Rgb color, float a) {
  const uint8_t* g = glyph(c);
  for (int row = 0; row < 7; row++) {
    uint8_t bits = g[row];
    for (int col = 0; col < 5; col++)
      if (bits & (1 << (4 - col))) fb.rect(x + col * 2, y + row * 2, 2, 2, color, a);
  }
}
