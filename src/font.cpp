namespace Font {

int textW(const char* s) { return (int)strlen(s) * 6 - 1; }
int text2xW(const char* s) { return (int)strlen(s) * 12 - 2; }

void text(Fb& fb, int x, int y, const char* s, Rgb color, float a) {
  int cx = x;
  for (const char* p = s; *p; p++) {
    drawChar(fb, cx, y, *p, color, a);
    cx += 6;
  }
}

void textCenter(Fb& fb, int cx, int y, const char* s, Rgb color, float a) {
  text(fb, (int)lroundf(cx - textW(s) / 2.0f), y, s, color, a);
}

void textRight(Fb& fb, int xRight, int y, const char* s, Rgb color, float a) {
  text(fb, xRight - textW(s), y, s, color, a);
}

void text2x(Fb& fb, int x, int y, const char* s, Rgb color, float a) {
  int cx = x;
  for (const char* p = s; *p; p++) {
    drawChar2x(fb, cx, y, *p, color, a);
    cx += 12;
  }
}

}  // namespace Font
